public class mientras {                         // Clase principal (requerida en Java)
    public static void main(String[] args) {    // Método principal (punto de entrada)
        
        int i = 1;                              // Variable contador inicia en 1
        
        while (i <= 5) {                        // Mientras i sea menor o igual a 5
            System.out.println(i);              // Imprimir valor de i
            i++;                                // Incrementar i en 1
        }
    }
}
