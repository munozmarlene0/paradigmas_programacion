#include <iostream>                             // Librería para entrada/salida
using namespace std;                            // Usar espacio de nombres estándar

int main() {                                    // Función principal del programa
    int numero = 5;                             // Variable que almacena el número    
    if (numero > 0) {                           // Si el número es mayor a 0
        cout << "Positivo" << endl;             // Imprimir "Positivo"
    } else {                                    // Si no se cumple la condición
        cout << "Negativo" << endl;             // Imprimir "Negativo"
    }
    return 0;                                   // Fin del programa
}
