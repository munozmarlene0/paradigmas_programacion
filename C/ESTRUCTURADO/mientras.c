#include <stdio.h>                              // Librería para entrada/salida

int main() {                                    // Función principal del programa
    int i = 1;                                  // Variable contador inicia en 1    
    while (i <= 5) {                            // Mientras i sea menor o igual a 5
        printf("%d\n", i);                      // Imprimir valor de i
        i++;                                    // Incrementar i en 1
    }    
    return 0;                                   // Fin del programa
}
