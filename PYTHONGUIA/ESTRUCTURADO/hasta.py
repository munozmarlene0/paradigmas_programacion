i = 1                                           # Variable contador inicia en 1

while True:                                     # Bucle infinito (simula do-while)
    print(i)                                    # Imprimir valor de i
    i += 1                                      # Incrementar i en 1
    if i > 5:                                   # Si i es mayor a 5
        break                                   # Salir del bucle

