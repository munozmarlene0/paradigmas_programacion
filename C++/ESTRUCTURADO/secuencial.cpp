#include <iostream>                             // Librería para entrada/salida
using namespace std;                            // Usar espacio de nombres estándar

int main() {
    float base = 5.0;                           // Paso 1: Declarar variable base
    float altura = 3.0;                         
    float area;                                 
    area = base * altura;                       // Paso 2: Calcular área (base por altura)  
    cout << "Área: " << area << endl;           // Paso 3: Mostrar resultado   
    return 0;                                   // Fin del programa
}
