public class secuencial {                       // Clase principal (requerida en Java)
    public static void main(String[] args) {    // Método principal (punto de entrada)
        
        float base = 5.0f;                      // Paso 1: Declarar variable base
        float altura = 3.0f;                    
        float area;                                
        area = base * altura;                   // Paso 2: Calcular área (base por altura)        
        System.out.println("Área: " + area);    // Paso 3: Mostrar resultado
    }
}
