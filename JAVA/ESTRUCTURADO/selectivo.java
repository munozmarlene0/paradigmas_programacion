public class selectivo {                        // Clase principal (requerida en Java)
    public static void main(String[] args) {    // Método principal (punto de entrada)        
        int numero = 5;                         // Variable que almacena el número        
        if (numero > 0) {                       // Si el número es mayor a 0
            System.out.println("Positivo");     // Imprimir "Positivo"
        } else {                                // Si no se cumple la condición
            System.out.println("Negativo");     // Imprimir "Negativo"
        }
    }
}
